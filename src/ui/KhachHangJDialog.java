    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        String TenKhachHang = JOptionPane.showInputDialog(this, "Nhập tên khách hàng cần tìm:");
        if (TenKhachHang != null && !TenKhachHang.trim().isEmpty()) {
            List<KhachHang> list = KhachHangDAO.SearchKhachHang(TenKhachHang.trim());
            DefaultTableModel model = (DefaultTableModel) tblKhachHang.getModel();
            model.setRowCount(0); // Xóa dữ liệu cũ

            for (KhachHang kh : list) {
                model.addRow(new Object[]{
                    kh.getID(),
                    kh.getTenKhachHang(),
                    kh.isGioiTinh() ? "Nam" : "Nữ",
                    kh.getNgaySinh(),
                    kh.getSoDienThoai()
                });
            }

            if (list.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Không tìm thấy khách hàng nào trùng tên.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập tên.");
        }
    }//GEN-LAST:event_btnTimKiemActionPerformed

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed

        if (txtTenKhachHang.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa nhập tên khách hàng");
            return;
        }
        if (!rdoNam.isSelected() && !rdoNu.isSelected()) {
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa chọn giới tính");
            return;
        }
        if (txtSDT.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa nhập số điện thoại");
            return;
        }
        if (dateNgaySinh.getDate() == null) {
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa chọn ngày sinh");
            return;
        }
        String TenKhachHang = txtTenKhachHang.getText();
        boolean GioiTinh = rdoNam.isSelected();
        Date NgaySinh = dateNgaySinh.getDate();
        String SoDienThoai = txtSDT.getText();
        KhachHang khachHang = new KhachHang(WIDTH, TenKhachHang, GioiTinh, NgaySinh, SoDienThoai);
        int ketQua = KhachHangDAO.CreateKhachHang(khachHang);
        if (ketQua == 1) {
            this.readKhachHang();
            JOptionPane.showMessageDialog(rootPane, "Thêm thành công");
            dateNgaySinh.setDate(null);  // Xóa giá trị ngày đã chọn
            txtTenKhachHang.setText("");
            buttonGroup1.clearSelection();

            txtSDT.setText("");
            tblKhachHang.clearSelection();
            return;
        } else {
            JOptionPane.showMessageDialog(rootPane, "Thêm thất bại");
            
            return;
        }


    }//GEN-LAST:event_btnThemActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        int selectedRow = tblKhachHang.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa chọn người cần xóa");
            return;
        }

        int id = (int) tblKhachHang.getValueAt(selectedRow, 0);

        KhachHang kh = new KhachHang();
        kh.setID(id);

        int ketqua = KhachHangDAO.DeleteKhachHang(kh);
        if (ketqua > 0) {
            JOptionPane.showMessageDialog(rootPane, "Xóa khách hàng thành công");
            this.readKhachHang();
            dateNgaySinh.setDate(null);  // Xóa giá trị ngày đã chọn
            txtTenKhachHang.setText("");
            buttonGroup1.clearSelection();

            txtSDT.setText("");
            tblKhachHang.clearSelection();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Xóa khách hàng thất bại");
            dateNgaySinh.setDate(null);  // Xóa giá trị ngày đã chọn
            txtTenKhachHang.setText("");
            buttonGroup1.clearSelection();

            txtSDT.setText("");
            tblKhachHang.clearSelection();
        }
        return;
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed

           int row = tblKhachHang.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn khách hàng để sửa");
            return;
        }
        int id = Integer.parseInt(tblKhachHang.getValueAt(row, 0).toString()); // lấy ID từ dòng

        String ten = txtTenKhachHang.getText();
        boolean gioiTinh = rdoNam.isSelected();
        Date ngaySinh = dateNgaySinh.getDate();
        String sdt = txtSDT.getText();

        KhachHang khachHang = new KhachHang(id, ten, gioiTinh, ngaySinh, sdt);
        int ketQua = KhachHangDAO.UpdateKhachHang(khachHang);
        if (ketQua > 0) {
            JOptionPane.showMessageDialog(rootPane, "Sửa thành công");
            readKhachHang();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Sửa  thất bại");
        }
        return;
    
    }//GEN-LAST:event_btnSuaActionPerformed

    private void tblKhachHangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblKhachHangMouseClicked
        int soHang = tblKhachHang.getSelectedRow();
        String TenKhachHang = tblKhachHang.getValueAt(soHang, 1).toString();
        String GioiTinh = tblKhachHang.getValueAt(soHang, 2).toString();
        String NgaySinh = tblKhachHang.getValueAt(soHang, 3).toString();
        String SoDienThoai = tblKhachHang.getValueAt(soHang, 4).toString();
        txtSDT.setText(SoDienThoai);
        txtTenKhachHang.setText(TenKhachHang);
        if (GioiTinh.equals("Nam")) {
            rdoNam.setSelected(true);
        } else {
            rdoNu.setSelected(true);
        }

        try {
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            Date ngaySinh = sdf.parse(NgaySinh);
            dateNgaySinh.setDate(ngaySinh);
        } catch (ParseException ex) {
            ex.printStackTrace();
        }

    }//GEN-LAST:event_tblKhachHangMouseClicked

