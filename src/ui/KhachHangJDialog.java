    public void readKhachHang() {
        List<KhachHang> list = KhachHangDAO.getAllKhachHang();
        //Lấy toàn bộ danh sách khách hàng add vào list
        DefaultTableModel tblKhachHang = (DefaultTableModel) this.tblKhachHang.getModel();
        //Ép kiểu về DefaultTableModel
        tblKhachHang.setRowCount(0);
        //Xóa toàn bộ dữ liệu đang có ở bảng 
        for (KhachHang khachHang : list) {
            //Duyệt từng khách hàng trong danh sách list 
            tblKhachHang.addRow(new Object[]{
                // add từng dòng 1 lên bảng theo thứ tự
                khachHang.getID(),
                khachHang.getTenKhachHang(),
                khachHang.isGioiTinh() ? "Nam" : "Nữ",
                khachHang.getNgaySinh(),
                khachHang.getSoDienThoai()

            });

        }
    }

